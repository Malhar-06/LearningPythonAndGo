# Break statement

numbers = [1, 2, 3, 4, 5]
for number in numbers:
    if number == 3:
        break
    print(number)


# Continue statement
    
numbers = [1, 2, 3, 4, 5]
for number in numbers:
    if number == 3:
        continue
    print(number)


# Pass statement

numbers = [1, 2, 3, 4, 5]
for number in numbers:
    if number == 3:
        pass
    print(number)